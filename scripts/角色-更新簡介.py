def update_note_with_biography(note: str, biography: str) -> str:
    """在 note 中加入或替換 <Biography> 區塊。"""
    bio_block = f"\n<Biography>\n<Wordwrap>{biography}\n</Biography>"

    # 如果已存在 Biography 區塊，替換之
    pattern = r'\n?<Biography>.*?</Biography>'
    if re.search(pattern, note, re.DOTALL):
        return re.sub(pattern, bio_block, note, flags=re.DOTALL)

    # 否則附加在 note 末尾
    return note.rstrip() + "\n" + bio_block


def main():
    with open(ACTORS_PATH, 'r', encoding='utf-8') as f:
        actors = json.load(f)

    updated = []

    for actor in actors:
        if actor is None:
            continue
        aid = actor['id']
        if aid not in CHARACTER_DATA:
            continue

        data = CHARACTER_DATA[aid]
        old_profile = actor.get('profile', '')
        old_note = actor.get('note', '')

        # 更新 profile
        actor['profile'] = data['profile']

        # 更新 biography（若有提供）
        if data['biography'] is not None:
            actor['note'] = update_note_with_biography(old_note, data['biography'])

        updated.append(f"  ID {aid:2d} {actor['name']}")

    # 寫回 JSON
    with open(ACTORS_PATH, 'w', encoding='utf-8') as f:
        json.dump(actors, f, ensure_ascii=False, indent=None)

    print(f"已更新 {len(updated)} 位角色：")
    for line in updated:
        print(line)


if __name__ == '__main__':
    main()
