def main():
    # Load existing weapons
    with open(WEAPONS_PATH, "r", encoding="utf-8") as f:
        weapons = json.load(f)

    print(f"Current weapon count: {len(weapons)} (null + {len(weapons)-1} entries)")

    # Generate new weapons starting from ID 151
    new_entries = []
    current_id = 151

    for sep_label, wtype_id, weapon_list in CHARACTERS:
        # Add separator
        new_entries.append(make_separator(current_id, sep_label))
        current_id += 1

        # Add 6 weapons
        for name, params, hit_pct, elem_pct, price, flavor in weapon_list:
            entry = make_weapon(current_id, name, wtype_id, params, hit_pct, elem_pct, price, flavor)
            new_entries.append(entry)
            current_id += 1

    print(f"Generated {len(new_entries)} new entries (IDs 151-{current_id-1})")
    print(f"  - {len(CHARACTERS)} characters × (1 separator + 6 weapons) = {len(new_entries)}")

    # Extend the weapons array to accommodate all new entries
    # Current array: [null, w1, w2, ..., w200] (201 elements, indices 0-200)
    # Need array to go up to index (current_id - 1), so total length = current_id
    target_length = current_id

    # First, ensure the array is large enough
    while len(weapons) < target_length:
        # Add empty placeholder entries
        placeholder_id = len(weapons)
        weapons.append({
            "id": placeholder_id,
            "animationId": 0,
            "description": "",
            "etypeId": 1,
            "traits": [{"code": 31, "dataId": 1, "value": 0}, {"code": 22, "dataId": 0, "value": 0}],
            "iconIndex": 0,
            "name": "",
            "note": "",
            "params": [0, 0, 0, 0, 0, 0, 0, 0],
            "price": 0,
            "wtypeId": 0
        })

    # Now replace entries at the correct indices
    for entry in new_entries:
        idx = entry["id"]  # ID matches array index
        weapons[idx] = entry

    # Write back
    with open(WEAPONS_PATH, "w", encoding="utf-8") as f:
        json.dump(weapons, f, ensure_ascii=False, separators=(",", ":"))

    print(f"\nFinal weapon array length: {len(weapons)}")
    print("Weapons.json updated successfully!")

    # Print summary
    print("\n=== Character Weapon Summary ===")
    for sep_label, wtype_id, weapon_list in CHARACTERS:
        char_name = sep_label.split("｜")[0]
        cfg = WTYPE_CONFIG[wtype_id]
        print(f"\n{char_name} ({cfg[0]}/{cfg[1]}):")
        for name, params, hit_pct, elem_pct, price, flavor in weapon_list:
            if cfg[4] == "atk":
                main_stat = f"ATK={params[2]}"
            elif cfg[4] == "mat":
                main_stat = f"MAT={params[4]}"
            elif cfg[4] == "def":
                main_stat = f"DEF={params[3]}"
            print(f"  {name:12s}  {main_stat:8s}  HIT={hit_pct}%  ELEM={elem_pct}%  ${price}")


if __name__ == "__main__":
    main()
