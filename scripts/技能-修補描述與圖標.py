def main():
    with open(SKILLS_PATH, "r", encoding="utf-8") as f:
        skills = json.load(f)

    icon_changed = 0
    desc_added = 0

    for skill in skills:
        if skill is None:
            continue
        sid = skill["id"]
        if sid < 12 or sid > 211:
            continue
        if skill["name"].startswith("----"):
            continue

        # 1) icon → 1509
        if skill["iconIndex"] != TARGET_ICON:
            skill["iconIndex"] = TARGET_ICON
            icon_changed += 1

        # 2) 加描述（僅空白者）
        if not skill["description"].strip() and sid in DESC_MAP:
            skill["description"] = DESC_MAP[sid]
            desc_added += 1
            print(f"  #{sid:3d} {skill['name']} → {DESC_MAP[sid][:20]}…")

    print(f"\n=== 完成 ===")
    print(f"  圖標改為 {TARGET_ICON}: {icon_changed}")
    print(f"  新增描述: {desc_added}")

    with open(SKILLS_PATH, "w", encoding="utf-8") as f:
        json.dump(skills, f, ensure_ascii=False, indent=None, separators=(",", ":"))
        f.write("\n")

    print(f"  已寫入 {SKILLS_PATH}")


if __name__ == "__main__":
    main()
