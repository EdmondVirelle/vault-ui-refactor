def main():
    # Load data
    with open(SKILLS_PATH, 'r', encoding='utf-8') as f:
        skills = json.load(f)
    with open(CLASSES_PATH, 'r', encoding='utf-8') as f:
        classes = json.load(f)

    # ── Part A: Fix 黃凱竹 Multi-Element tags ──
    print("Part A: Fixing 黃凱竹 Multi-Element tags (奇門→弓術)...")
    fixed_a = 0
    for sid in range(1583, 1593):
        sk = skills[sid]
        if sk and '奇門' in sk.get('note', ''):
            sk['note'] = sk['note'].replace('奇門', '弓術')
            fixed_a += 1
        if sk and '奇門' in sk.get('description', ''):
            sk['description'] = sk['description'].replace('奇門', '弓術')
            fixed_a += 1
    print(f"  Fixed {fixed_a} occurrences in skills 1583-1592")

    # ── Part B: Fix EquipWeapon traits ──
    print("Part B: Fixing EquipWeapon traits...")
    for cls in classes:
        if cls is None:
            continue
        cid = cls['id']
        if cid not in EQUIP_FIXES:
            continue
        new_weapons = EQUIP_FIXES[cid]
        # Remove all existing code=51 traits
        old_traits = [t for t in cls['traits'] if t['code'] != 51]
        # Add new weapon traits
        for wid in new_weapons:
            old_traits.append({"code": 51, "dataId": wid, "value": 0})
        cls['traits'] = old_traits
        print(f"  Class {cid} ({cls['name']}): weapons → {new_weapons}")

    # ── Part C: Generate new skills ──
    print("Part C: Generating 276 job-specific skill entries...")
    new_count = 0
    for char_idx, (char_name, cids, branch_names, skill_defs) in enumerate(CHARS):
        base_id = 1604 + char_idx * 12
        for branch_idx in range(3):
            # Separator
            sep_id = base_id + branch_idx * 4
            sep = mk_sep(sep_id, branch_names[branch_idx])
            skills[sep_id] = sep
            new_count += 1
            # 3 skills per branch
            for skill_idx in range(3):
                sid = sep_id + 1 + skill_idx
                sk_data = skill_defs[branch_idx * 3 + skill_idx]
                sk = _mk(sid, *sk_data)
                skills[sid] = sk
                new_count += 1
    print(f"  Generated {new_count} entries (IDs 1604-1879)")

    # ── Part D: Update class learnings ──
    print("Part D: Updating class learnings...")
    levels = [15, 25, 35]
    for char_idx, (char_name, cids, branch_names, _) in enumerate(CHARS):
        base_id = 1604 + char_idx * 12
        for branch_idx, cid in enumerate(cids):
            # Find class entry
            cls = classes[cid]
            if cls is None:
                print(f"  WARNING: Class {cid} is null, skipping")
                continue
            # Skill IDs for this branch (offset +1,+2,+3 from separator)
            skill_ids = [base_id + branch_idx * 4 + 1 + i for i in range(3)]
            # Append new learnings (preserve existing)
            for lvl, skid in zip(levels, skill_ids):
                cls['learnings'].append({"level": lvl, "note": "", "skillId": skid})
            print(f"  Class {cid} ({cls['name']}): +skills {skill_ids}")

    # ── Write output ──
    print("\nWriting Skills.json...")
    _write_rpgmaker_json(SKILLS_PATH, skills)
    print("Writing Classes.json...")
    _write_rpgmaker_json(CLASSES_PATH, classes)
    print("Done!")


def _write_rpgmaker_json(path, data):
    """Write JSON in RPG Maker MZ format (one entry per line)."""
    lines = ['[']
    for i, entry in enumerate(data):
        comma = ',' if i < len(data) - 1 else ''
        if entry is None:
            lines.append(f'null{comma}')
        else:
            lines.append(json.dumps(entry, ensure_ascii=False, separators=(',', ':')) + comma)
    lines.append(']')
    with open(path, 'w', encoding='utf-8', newline='\n') as f:
        f.write('\n'.join(lines) + '\n')


